<h3>Extra vragen</h3>
<p>
    Met deze extra vragen kun je bonuspunten verdienen. Zodra de uitslag vna de finale bekend is, zullen de punten voor deze vragen worden meegnomen in de berekening. Vul hier je antwoorden op de extra vragen in <span class='bold'>voordat het toernooi begint</span>.
</p>
<p>
    Voor sommige vragen krijg je zelfs punten als je het antwoord niet exact goed hebt! Hoe dichter erbij, hoe meer punten je verdient!
</p>