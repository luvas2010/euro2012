<h3>Extra vragen</h3>
<p>
    Dit is het beheerscherm voor de extra vragen. Formuleer in de kolom 'Vraag' de vraag, en geef het <span class='bold'>juiste antwoord</span> in de kolom antwoord.
</p>
<p>
    Bij een vraag van het type <span class='bold'>'keuze'</span> geef je de mogelijke antwoorden gescheiden door een komma. Zodra het juiste antwoord bekend is, zet je een "+" teken achter die keuze (zoals in de voorbeelden). Zodra je de uitslag van de finale hebt ingevoerd, worden de punten voor de extra vragen automatisch meegenomen als je de punten gaat berekenen.
</p>
<p>
    Voor vragen van het type <span class='bold'>"benadering"</span> wordt de volgende berekenings methode gehanteerd: Een gebruiker krijgt de maximale punten als het antwoord exact gegeven is. Zit hij er &eacute;&eacute;n boven of onder, krijgt die gebuiker &eacute;&eacute;n punt minder dan het maximale aantal, bij een verschil van twee krijgt men twee punten minder, enz. Als je de vraag dus waardeert met 10 punten, kan men nog &eacute;&eacute;n punt krijgen als het antwoord tien punten afwijkt.
</p>
<p>
    Als je een extra vraag hebt geformuleerd, moet je die vraag ook nog op 'aktief' zetten.
</p>
<p>
    Deelnemers die voor het begin van het toernooi nog geen antwoord hebben gegeven, krijgen een waarschuwing op hun 'home page'.
</p>